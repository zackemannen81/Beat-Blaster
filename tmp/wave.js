function normalizePlaylist(fallbackId, raw) {
  if (typeof raw === "object" && raw !== null && "waves" in raw) {
    const data = raw;
    return {
      id: typeof data.id === "string" && data.id.length > 0 ? data.id : fallbackId,
      waves: Array.isArray(data.waves) ? data.waves : []
    };
  }
  return {
    id: fallbackId,
    waves: []
  };
}
var library = {
  easy: normalizePlaylist("easy", easy_default),
  normal: normalizePlaylist("normal", normal_default),
  hard: normalizePlaylist("hard", hard_default),
  wip: normalizePlaylist("wip", wip_default)
};
function getWavePlaylist(profileId) {
  return library[profileId];
}
